#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned const char	*buf_src;
	size_t				i;

	i = 0;
	buf_src = s;
	while (i < n)
	{
		if (buf_src[i] == (unsigned char)c)
			return ((void *)(buf_src + i));
		i++;
	}
	return (NULL);
}
