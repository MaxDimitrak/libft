#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*buf_pointer;

	buf_pointer = (void *)malloc(nmemb * size);
	if (!buf_pointer)
		return (NULL);
	ft_bzero(buf_pointer, nmemb * size);
	return (buf_pointer);
}
